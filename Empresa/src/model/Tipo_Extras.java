package model;

public class Tipo_Extras {
	private int idExtras;
	private String nomExtras;
	public Tipo_Extras(int idExtras, String nomExtras) {
		super();
		this.idExtras = idExtras;
		this.nomExtras = nomExtras;
	}
	public int getIdExtras() {
		return idExtras;
	}
	public void setIdExtras(int idExtras) {
		this.idExtras = idExtras;
	}
	public String getNomExtras() {
		return nomExtras;
	}
	public void setNomExtras(String nomExtras) {
		this.nomExtras = nomExtras;
	}
	
	

}
